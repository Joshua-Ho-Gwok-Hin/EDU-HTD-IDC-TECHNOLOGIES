package com.corejava;

public class LoopingStatement {

    public static void main(String[] args) {

        int count=0;
        while (count < 10) {
            count++;
            System.out.println("Count = " + count);
        }


        for (int i = 1; i < 11; i++) {
            System.out.println("i loop " + i);
        }


    }
}
